class Program14{
	public static void main(String[] args)
	{
		int a=10,b=20;

		System.out.println("hello"+a);//concatination
		System.out.println(a+"hello");//concatination is used to join string with other different values using Plus(+)
		System.out.println("hello"+a+b);// in this case 'hello' string and a is integer
		System.out.println(a+b+"hello");
		System.out.println(a+"hello"+b);
	}
}